/* String manipulation
    -Refers to the different ways we can act on strings to use them 
    or prepare them for a specified use


    With operators:
    
        -Some operators can be used to manipulate strings or compare them to other strings and values 
    
            Examples:
            
            -the + operator can be used to concatenate strings
            
            -the == operator can be used to compare a string to 
            another value to assess matching or derive a boolean value
    
    With string methods
    
        -In the global object they are a number of functions that can be used to manipulate strings
        
            -Exmples:
            
            concat()	Joins two or more strings, and returns a new joined strings
            endsWith()	Checks whether a string ends with specified string/characters
            match()	Searches a string for a match against a regular expression, and returns the matches
            replace()	Searches a string for a specified value, or a regular expression, and returns a new string where the specified values are replaced
            search()	Searches a string for a specified value, or regular expression, and returns the position of the match
            slice()	Extracts a part of a string and returns a new string
            split()	Splits a string into an array of substrings
            startsWith()	Checks whether a string begins with specified characters
            substr()	Extracts the characters from a string, beginning at a specified start position, and through the specified number of character
            substring()	Extracts the characters from a string, between two specified indices
            toLowerCase()	Converts a string to lowercase letters
            toString()	Returns the value of a String object
            toUpperCase()	Converts a string to uppercase letters
            valueOf()	Returns the primitive value of a String object
            **and many more!

*/ 