/*
Control flow
   
    In JavaScript we can use logical language to tell the interpreter HOW or IF to 
    execute snippets of code. This process is called control flow and is controlled 
    by statements resolving to true or false.
    You can make the chains as long as is needed to eliminate all use cases.

The following is a structure the interpreter recognizes:

        if (condition1 (compared to) condition2) {
            return result;
        } else if (condition1 (compared to) condition2) {
            return result;
        } else {
            return result;
        }
        


*/